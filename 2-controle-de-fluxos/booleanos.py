# ENTENDENDO COMPARAÇÕES DE VARIÁVEIS #

print('hello' == 'hello')  # true
print('hello' == 'Hello')  # false
print('dog' != 'cat')  # true
print(42 == 42.0)  # true
print(42 == '42')  # false

print(not True)  # false
print(not not True)  # true

print(2+2 == 4 and not 2 + 2 == 5 and 2 * 2 == 2 + 2)  # true
